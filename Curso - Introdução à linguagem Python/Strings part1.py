# Strings part1

a = "Diego"
b = "Mariano"

concatenar = a + " " + b # irá cacatenar na mesma linha a variável a + b
print (concatenar)

tamanho = len(concatenar) # irá contar a quantidade de caractere q possui na variável concatenar
print (tamanho)

print (a[2]) # irá verificar o caractere que tem na variável a que ocupa a posição 2, lembrando que conta de 0 então será na posição 3, em teoria

print (concatenar [0:9]) # irá imprimir o resultado de concatenar do caractere 0 ao caractere 9
