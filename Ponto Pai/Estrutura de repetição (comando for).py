# Estrutura de repetição (comando for)

lista1 = [1, 2, 3, 4]
lista2 = ["Olá", "Mundo", "!"]
lista3 = [0,"bola", "biscoito", "bolacha", 9.99, True]

for i in lista1:
	print (i) # irá imprimir a lista1

for i in lista2:
	print (i) # irá imprimir a lista2

for i in lista3:
	print (i) # irá imprimir a lista3